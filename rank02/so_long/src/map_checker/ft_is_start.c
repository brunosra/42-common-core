#include "../so_long.h"

int	ft_is_start(t_map *map)
{
	int	i;
	int	j;
	int	c;

	i = 0;
	c = 0;
	while (i < map->h)
	{
		j = 0;
		while (j < map->w)
			if (map->data[i][j++] == 'P')
				c++;
		i++;
	}
	if (c != 1)
		return (0);
	return (1);
}
